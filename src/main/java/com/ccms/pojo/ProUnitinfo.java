package com.ccms.pojo;


public class ProUnitinfo {

  private String id;
  private String name;
  private String unitTypeId;
  private String unitGradeId;
  private String outsideCode;
  private int delstate;
  private String areaId;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getUnitTypeId() {
    return unitTypeId;
  }

  public void setUnitTypeId(String unitTypeId) {
    this.unitTypeId = unitTypeId;
  }


  public String getUnitGradeId() {
    return unitGradeId;
  }

  public void setUnitGradeId(String unitGradeId) {
    this.unitGradeId = unitGradeId;
  }


  public String getOutsideCode() {
    return outsideCode;
  }

  public void setOutsideCode(String outsideCode) {
    this.outsideCode = outsideCode;
  }


  public int getDelstate() {
    return delstate;
  }

  public void setDelstate(int delstate) {
    this.delstate = delstate;
  }


  public String getAreaId() {
    return areaId;
  }

  public void setAreaId(String areaId) {
    this.areaId = areaId;
  }

}
