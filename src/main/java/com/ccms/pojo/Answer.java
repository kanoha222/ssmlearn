package com.ccms.pojo;


public class Answer {

  private String id;
  private String nid;
  private String uid;
  private int flag;
  private int vote;
  private String reson;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getNid() {
    return nid;
  }

  public void setNid(String nid) {
    this.nid = nid;
  }


  public String getUid() {
    return uid;
  }

  public void setUid(String uid) {
    this.uid = uid;
  }


  public int getFlag() {
    return flag;
  }

  public void setFlag(int flag) {
    this.flag = flag;
  }


  public int getVote() {
    return vote;
  }

  public void setVote(int vote) {
    this.vote = vote;
  }


  public String getReson() {
    return reson;
  }

  public void setReson(String reson) {
    this.reson = reson;
  }

}
